namespace JudgeSharp.Views
{
    /// <summary>
    /// Interaction logic for AboutView.xaml
    /// </summary>
    public partial class AboutView : UserControl
    {

        public AboutView()
        {
            InitializeComponent();
            AboutTextBlock.Text = ReadResourceTxtFile("JudgeSharp.About.txt");
            LogoTextBlock.Text = ReadResourceTxtFile("JudgeSharp.Logo.txt");
            GithubButton.Content = "https://github.com/BasharKernel/JudgeSharp";
        }

        private static string ReadResourceTxtFile(string resourceName)
        {
            var assembly = Assembly.GetExecutingAssembly();
            var n = assembly.GetManifestResourceNames();
            using (System.IO.Stream stream = assembly.GetManifestResourceStream(resourceName))
            using (System.IO.StreamReader reader = new System.IO.StreamReader(stream))
            {
                string result = reader.ReadToEnd();
                return result;
            }
        }

        private void GithubButton_Click(object sender, RoutedEventArgs e)
        {
            string link = GithubButton.Content as string;
            if (link == null)
                return;
            System.Diagnostics.Process.Start(link);
        }
    }
